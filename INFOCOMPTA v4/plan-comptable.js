    function getLevel(code) {
      const trimmed = code.trim();
      const len = trimmed.replace(/\D/g,'').length || trimmed.length;
      if (len <= 1) return 1;
      if (len === 2) return 2;
      if (len === 3) return 3;
      if (len === 4) return 4;
      return 5;
    }
    
    function getClass(code) {
      return code.trim()[0];
    }
    
    function renderLines() {
      const container = document.getElementById('lines');
      container.innerHTML = '';
      const q = currentSearch.toLowerCase();
      let visible = 0;
    
      data.forEach(([code, label]) => {
        const cls = getClass(code);
        const classMatch = currentClass === 'all' || cls === currentClass;
        const searchMatch = !q || code.toLowerCase().includes(q) || label.toLowerCase().includes(q);
        const show = classMatch && searchMatch;
        const level = getLevel(code);
    
        const div = document.createElement('div');
        div.className = 'line cls-' + cls + (show ? '' : ' hidden');
        div.dataset.level = level;
    
        const codeSpan = document.createElement('span');
        codeSpan.className = 'lcode';
        const labelSpan = document.createElement('span');
        labelSpan.className = 'llabel';
    
        if (q && searchMatch) {
          codeSpan.innerHTML = hl(code, q);
          labelSpan.innerHTML = hl(label, q);
        } else {
          codeSpan.textContent = code;
          labelSpan.textContent = label;
        }
    
        // Bouton détail
        const infoBtn = document.createElement('a');
        infoBtn.href = `compte-detail.html?code=${code}`;
        infoBtn.className = 'info-btn';
        infoBtn.title = `Voir le fonctionnement du compte ${code}`;
        infoBtn.textContent = 'ℹ';
    
        div.appendChild(codeSpan);
        div.appendChild(labelSpan);
        div.appendChild(infoBtn);
        container.appendChild(div);
        if (show) visible++;
      });
    
      const total = data.filter(([c]) => currentClass === 'all' || getClass(c) === currentClass).length;
      document.getElementById('count').textContent =
        q ? `${visible} résultat(s) sur ${total}` : `${total} comptes`;
    }
    
    function hl(text, q) {
      const re = new RegExp(`(${q.replace(/[.*+?^${}()|[\]\\]/g,'\\$&')})`, 'gi');
      return text.replace(re, '<mark class="highlight">$1</mark>');
    }
    
    function filterLines(val) {
      currentSearch = val.trim();
      renderLines();
    }
    
    function filterClass(cls, btn) {
      currentClass = cls;
      document.querySelectorAll('.tab').forEach(t => t.classList.remove('active'));
      btn.classList.add('active');
      renderLines();
    }
    
